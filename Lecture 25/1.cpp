#include <iostream> 
using namespace std; 

int main() { 
    // Initialize variables 
    int num1 = 8; 
    int num2 = 5; 

    // Comparisons using relational operators
    cout << "num1 > num2 = " << (num1 > num2) << endl;  // Greater than
    cout << "num1 < num2 = " << (num1 < num2) << endl;  // Less than
    cout << "num1 == num2 = " << (num1 == num2) << endl; // Equal to
    cout << "num1 != num2 = " << (num1 != num2) << endl; // Not equal to

    return 0; 
}