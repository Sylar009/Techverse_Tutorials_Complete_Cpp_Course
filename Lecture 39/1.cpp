#include <iostream>
using namespace std;

int main() {
    int number = 20;  // Initializing the number

    if (number == 10) {
        cout << "The number is 10\n";
    } else if (number == 15) {
        cout << "The number is 15\n";
    } else if (number == 20) {
        cout << "The number is 20\n";
    } else {
        cout << "The number does not match any condition\n";
    }

    return 0;
}