#include <iostream>  
using namespace std;  

int main() {  
    float f = 2.7f;  // Float literal  
    double d = 3.1415;  // Double literal  
    long double ld = 9.8L;  // Long double literal  
    cout << "Float: " << f << "\nDouble: " << d << "\nLong Double: " << ld << endl;  
    return 0;  
}  