#include <iostream>
using namespace std;

int main() {
    int num = 10; // Initial value
    num += 5; // Adding 5
    cout << "After num += 5: " << num << endl;

    num -= 3; // Subtracting 3
    cout << "After num -= 3: " << num << endl;

    num *= 4; // Multiplying by 4
    cout << "After num *= 4: " << num << endl;

    num /= 2; // Dividing by 2
    cout << "After num /= 2: " << num << endl;

    return 0;
}