#include <iostream>
using namespace std;

int main() {
    int num1 = 8, num2 = 3;
    
    // Addition
    cout << "Sum: " << (num1 + num2) << endl;
    
    // Subtraction
    cout << "Difference: " << (num1 - num2) << endl;

    // Increment
    cout << "Increment: " << (++num1) << endl;

    // Decrement
    cout << "Decrement: " << (--num2) << endl;

    return 0;
}