#include <iostream>
using namespace std;

int main() {
    int x = 5, y = 10;

    // Checking greater
    cout << "Is x > y? " << (x > y) << endl;

    // Checking equality
    cout << "Is x == y? " << (x == y) << endl;

    // Not equal
    cout << "Is x != y? " << (x != y) << endl;

    return 0;
}