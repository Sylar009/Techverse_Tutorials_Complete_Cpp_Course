#include <iostream>
using namespace std;

int main() {
    int number = 20;  // Variable initialized with 20

    if (number == 10) {  // Check if number is equal to 10
        cout << "The number is 10\n";
    } else {
        cout << "The number is not 10\n";  // Executes if condition is false
    }

    cout << "Execution completed.\n";
    return 0;
}