#include <iostream>
using namespace std;

int main() {
    int value = 25;  // Initialize variable with 25

    if (value > 15) {  // Check if value is greater than 15
        cout << "Value is greater than 15\n";
    } else {
        cout << "Value is smaller than 15\n";  // Executes if condition is false
    }

    return 0;
}